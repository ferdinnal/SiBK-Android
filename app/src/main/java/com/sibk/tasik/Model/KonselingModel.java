package com.sibk.tasik.Model;

public class KonselingModel {
    private int kondid;
    private int userid;
    private String subject;
    private String tg;
    private String wk;
    private String st;

    public int getKondid() {
        return kondid;
    }

    public void setKondid(int kondid) {
        this.kondid = kondid;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getTg() {
        return tg;
    }

    public void setTg(String tg) {
        this.tg = tg;
    }

    public String getWk() {
        return wk;
    }

    public void setWk(String wk) {
        this.wk = wk;
    }

    public String getSt() {
        return st;
    }

    public void setSt(String st) {
        this.st = st;
    }


}
