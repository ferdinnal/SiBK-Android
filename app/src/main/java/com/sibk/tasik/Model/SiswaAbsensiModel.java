package com.sibk.tasik.Model;

public class SiswaAbsensiModel {
    private String nisn;
    private String namaSiswa;
    private String statusAbsen;
    private int absensiId;
    private int statusAbsenId;
    private int idSiswa;

    public String getNisn() {
        return nisn;
    }

    public void setNisn(String nisn) {
        this.nisn = nisn;
    }

    public String getNamaSiswa() {
        return namaSiswa;
    }

    public void setNamaSiswa(String namaSiswa) {
        this.namaSiswa = namaSiswa;
    }

    public String getStatusAbsen() {
        return statusAbsen;
    }

    public void setStatusAbsen(String statusAbsen) {
        this.statusAbsen = statusAbsen;
    }

    public int getAbsensiId() {
        return absensiId;
    }

    public void setAbsensiId(int absensiId) {
        this.absensiId = absensiId;
    }

    public int getStatusAbsenId() {
        return statusAbsenId;
    }

    public void setStatusAbsenId(int statusAbsenId) {
        this.statusAbsenId = statusAbsenId;
    }

    public int getIdSiswa() {
        return idSiswa;
    }

    public void setIdSiswa(int idSiswa) {
        this.idSiswa = idSiswa;
    }


}
